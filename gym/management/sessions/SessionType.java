package gym.management.sessions;
/**
 * Enum representing the different types of sessions offered in the gym.
 */
public enum SessionType {
    /**
     * Represents a Thai Boxing session.
     */
    ThaiBoxing,

    /**
     * Represents a Pilates session.
     */
    Pilates,

    /**
     * Represents a Machine Pilates session.
     */
    MachinePilates,

    /**
     * Represents a Ninja session.
     */
    Ninja;
}
